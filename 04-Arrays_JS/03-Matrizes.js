// Arrays Bidimensionais (Matrizes)

// Criando uma matriz bidimensional de 2x2 (2 linhas e 2 colunas)
var matriz = [
    [1, 2],
    [3, 4]
];

// Acessando elementos da matriz
console.log(matriz[0][0]); // Acessa o elemento na primeira linha e primeira coluna (1)
console.log(matriz[0][1]); // Acessa o elemento na primeira linha e segunda coluna (2)
console.log(matriz[1][0]); // Acessa o elemento na segunda linha e primeira coluna (3)
console.log(matriz[1][1]); // Acessa o elemento na segunda linha e segunda coluna (4)