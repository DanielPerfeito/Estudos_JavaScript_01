// Exemplo do Operador Ternário

// Suponha que temos uma variável 'idade' com um valor de 20
var idade = 20;

// Utilizando o operador ternário para determinar se a pessoa é maior de idade
// Se a idade for maior ou igual a 18, a variável 'status' receberá a string "Maior de Idade"; caso contrário, receberá a string "Menor de Idade"
var status = idade >= 18 ? "Maior de Idade" : "Menor de Idade";

// Exibe o status no console
console.log("A pessoa é " + status + ".");