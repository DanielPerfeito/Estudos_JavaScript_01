// Exemplo de estrutura condicional if...else em JavaScript

// Declaração da variável 'idade' e atribuição de um valor (idade fictícia)
var idade = 20;

// Verifica se a idade é maior ou igual a 18 anos
if (idade >= 18) {
    console.log("Você é maior de idade."); // Se a condição for verdadeira, imprime esta mensagem
} else {
    console.log("Você é menor de idade."); // Se a condição não for verdadeira, imprime esta mensagem
}

// Neste exemplo, a variável 'idade' é verificada para determinar se a pessoa é maior ou menor de idade.

// Se a idade for maior ou igual a 18, a mensagem "Você é maior de idade." é impressa.

// Caso contrário, a mensagem "Você é menor de idade." é impressa.

// Neste código, você pode ver como a estrutura condicional if...else é usada para tomar decisões com base em uma condição. Se a condição especificada dentro do if for verdadeira, o código dentro do bloco if é executado; caso contrário, o código dentro do bloco else é executado. Neste caso, estamos verificando se a idade é maior ou igual a 18 e exibindo uma mensagem correspondente com base nessa verificação.