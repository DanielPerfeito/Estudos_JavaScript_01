// STRINGS

// Declaração de variáveis
var numero = "8";
var str = "valor qualquer ";

// Concatenando a string 'numero' à variável 'str'
str += numero; // Agora, 'str' contém "valor qualquer 8"

// Imprimindo as variáveis no terminal
console.log(numero); // Imprime "8" no terminal
console.log(str);    // Imprime "valor qualquer 8" no terminal