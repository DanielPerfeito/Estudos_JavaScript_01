// OPERADORES DE COMPARAÇÃO:

// == Igual a
// Compara se dois valores são iguais, mas não verifica o tipo de dado.
var igualA = 5 == "5"; // Verdadeiro, pois os valores são iguais

// === Valor igual e igual
// Compara se dois valores são iguais em valor e tipo de dado.
var valorIgual = 5 === "5"; // Falso, pois os tipos de dados são diferentes

// != Não é igual
// Compara se dois valores não são iguais, mas não verifica o tipo de dado.
var naoIgual = 6 != "3"; // Verdadeiro, pois os valores são diferentes

// !== Não igual ou não igual
// Compara se dois valores não são iguais em valor ou tipo de dado.
var valorNaoIgual = 6 !== "6"; // Verdadeiro, pois os tipos de dados são diferentes

// > Maior que
// Compara se o valor à esquerda é maior que o valor à direita.
var maiorQue = 8 > 4; // Verdadeiro, pois 8 é maior que 4

// < Menor que
// Compara se o valor à esquerda é menor que o valor à direita.
var menorQue = 3 < 7; // Verdadeiro, pois 3 é menor que 7

// >= Maior que ou igual a
// Compara se o valor à esquerda é maior ou igual ao valor à direita.
var maiorOuIgualA = 5 >= 5; // Verdadeiro, pois 5 é igual a 5

// <= Menor que ou igual a
// Compara se o valor à esquerda é menor ou igual ao valor à direita.
var menorOuIgualA = 4 <= 3; // Falso, pois 4 não é menor ou igual a 3
