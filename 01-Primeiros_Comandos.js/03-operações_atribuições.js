// OPERAÇÕES E ATRIBUIÇÕES:

// Adição (+)
var soma = 5 + 3; // O resultado é 8

// Subtração (-)
var subtracao = 10 - 4; // O resultado é 6

// Multiplicação (*)
var multiplicacao = 6 * 7; // O resultado é 42

// Divisão (/)
var divisao = 15 / 3; // O resultado é 5

// Módulo (%)
var modulo = 10 % 3; // O resultado é 1 (resto da divisão de 10 por 3)

// Incremento (++)
var contador = 5;
contador++; // Incrementa em 1, agora contador é 6

// Decremento (--)
var outroContador = 8;
outroContador--; // Decrementa em 1, agora outroContador é 7