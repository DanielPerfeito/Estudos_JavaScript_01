// TIPOS DE DADOS:

// BOOLEAN (Lógico)
// Pode ser true (verdadeiro) ou false (falso). Usado para expressar condições.
var isTrue = true;
var isFalse = false;

// NULL (Nulo)
// Representa a ausência de valor intencionalmente atribuído.
var emptyValue = null;

// UNDEFINED (Indefinido)
// Geralmente indica uma variável que foi declarada, mas ainda não possui valor.
var undefinedValue;

// NUMBER (Número)
// Representa valores numéricos inteiros ou de ponto flutuante.
var integerNumber = 42;
var floatingPointNumber = 3.14;

// STRING (Texto)
// Representa sequências de caracteres, como palavras ou frases.
var text = "Isso é uma string";