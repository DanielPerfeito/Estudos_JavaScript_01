// Usando prompt para obter entrada do usuário e armazenar em uma variável
var nome = prompt("Qual é o seu nome?");

// Exibe uma mensagem de boas-vindas usando alert
alert("Bom dia, " + nome + "!");

// O código acima exibe um prompt para o usuário inserir seu nome.
// O valor inserido pelo usuário é armazenado na variável 'nome'.
// Em seguida, o alert exibe uma mensagem de boas-vindas com o nome inserido.
// Por exemplo, se o usuário inserir "Maria", o alert exibirá "Bom dia, Maria!".