// Exemplo da Estrutura de Repetição FOR

// Vamos imprimir os números de 1 a 5
for (let i = 1; i <= 5; i++) {
    // A variável de controle 'i' começa em 1.
    // O loop continuará enquanto 'i' for menor ou igual a 5.
    // Após cada iteração, 'i' é incrementada em 1.
    
    console.log("Número: " + i);
    // Exibe o valor atual de 'i' no console a cada iteração.
}