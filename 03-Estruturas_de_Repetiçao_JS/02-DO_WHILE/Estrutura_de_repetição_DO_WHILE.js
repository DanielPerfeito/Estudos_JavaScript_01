// Exemplo da Estrutura de Repetição DO...WHILE

// Suponha que queremos contar até 5
let contador = 1;

// Utilizando a estrutura 'do...while' para repetir uma ação pelo menos uma vez e continuar enquanto a condição for verdadeira
do {
    console.log("Contagem: " + contador);
    contador++; // Incrementa o contador
} while (contador <= 5); // A condição vem no final.

console.log("Fim da contagem.");