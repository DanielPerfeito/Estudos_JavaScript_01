// Exemplo da Estrutura de Repetição WHILE

// Suponha que queremos contar até 5
let contador = 1;

// Usando a estrutura 'while' para repetir uma ação enquanto a condição for verdadeira
while (contador <= 5) {
    console.log("Contagem: " + contador);
    contador++; // Incrementa o contador para evitar um loop infinito
}

console.log("Fim da contagem.");