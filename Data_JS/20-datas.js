// DATAS - Formato padrão (year, month, day, hours, minutes, seconds, milliseconds)

// Data atual
var d1 = new Date();

// Exibir a data atual no console
console.log("Data atual:", d1);

// Criar uma data personalizada usando números
var d2 = new Date(2022, 10, 15);

// Exibir a data personalizada criada com números no console
console.log("Data personalizada (números):", d2);

// Criar uma data personalizada usando strings
var d3 = new Date("Nov 15 2022 15:00");

// Exibir a data personalizada criada com uma string no console
console.log("Data personalizada (strings):", d3);